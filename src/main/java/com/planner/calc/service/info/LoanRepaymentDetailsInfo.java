package com.planner.calc.service.info;

public class LoanRepaymentDetailsInfo {

	private double openingAmount;
	private double rate;
	private double emi;
	private double interestForPeriod;
	private double principalForPeriod;
	private double prePaymentAmout;
	private double outstandingAmount;
	public double getOpeningAmount() {
		return openingAmount;
	}
	public void setOpeningAmount(double openingAmount) {
		this.openingAmount = openingAmount;
	}
	public double getRate() {
		return rate;
	}
	public void setRate(double rate) {
		this.rate = rate;
	}
	public double getEmi() {
		return emi;
	}
	public void setEmi(double emi) {
		this.emi = emi;
	}
	public double getInterestForPeriod() {
		return interestForPeriod;
	}
	public void setInterestForPeriod(double interestForPeriod) {
		this.interestForPeriod = interestForPeriod;
	}
	public double getPrincipalForPeriod() {
		return principalForPeriod;
	}
	public void setPrincipalForPeriod(double principalForPeriod) {
		this.principalForPeriod = principalForPeriod;
	}
	public double getPrePaymentAmout() {
		return prePaymentAmout;
	}
	public void setPrePaymentAmout(double prePaymentAmout) {
		this.prePaymentAmout = prePaymentAmout;
	}
	public double getOutstandingAmount() {
		return outstandingAmount;
	}
	public void setOutstandingAmount(double outstandingAmount) {
		this.outstandingAmount = outstandingAmount;
	}
	
	
}
